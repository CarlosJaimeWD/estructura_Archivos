    private void closeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_closeButtonActionPerformed
        System.out.println(contenedorTP.getSelectedComponent());
        System.out.println(contenedorTP.getSelectedIndex());
        int selectedIndex = contenedorTP.getSelectedIndex();
        editorClass = (Editor) contenedorTP.getSelectedComponent();
        System.out.println(editorClass.getFile().getName());
        

    int cerrar = JOptionPane.showConfirmDialog(contenedorTP, "Desea cerrar sin guardar?", "Cerrar", JOptionPane.YES_NO_OPTION);
        if (cerrar == JOptionPane.NO_OPTION) {
            editorClass = (Editor) contenedorTP.getSelectedComponent();
            String content = editorClass.getjEditorPane1().getText();

            try (FileWriter writer = new FileWriter(editorClass.getFile().getName());
                BufferedWriter bw = new BufferedWriter(writer)) {
                bw.write(content);
            } catch (IOException e) {
                System.err.format("IOException: %s%n", e);
            }
        }
        contenedorTP.remove(selectedIndex);
        //
    }//GEN-LAST:event_closeButtonActionPerformed


    private void saveAsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAsButtonActionPerformed
        String nombreArchivo = JOptionPane.showInputDialog("Guardar como?");
        String extensionArchivo = ".txt";
        String nombreCompleto = nombreArchivo.concat(extensionArchivo);
        
        editorClass = (Editor) contenedorTP.getSelectedComponent();
        String content = editorClass.getjEditorPane1().getText();

        try (FileWriter writer = new FileWriter(nombreCompleto);
            BufferedWriter bw = new BufferedWriter(writer)) {
            bw.write(content);
            contenedorTP.setTitleAt(contenedorTP.getSelectedIndex(), nombreArchivo);
        } catch (IOException e) {
            System.err.format("IOException: %s%n", e);
        }
    }//GEN-LAST:event_saveAsButtonActionPerformed

    private void saveAllButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAllButtonActionPerformed
        //System.out.println(contenedorTP.getTabCount());
        for (int i = 0; i < contenedorTP.getTabCount(); i++) {
            editorClass = (Editor) contenedorTP.getComponentAt(i);
            String content = editorClass.getjEditorPane1().getText();

            try (FileWriter writer = new FileWriter(editorClass.getFile().getName());
                BufferedWriter bw = new BufferedWriter(writer)) {
                bw.write(content);
            } catch (IOException e) {
                System.err.format("IOException: %s%n", e);
            }
        }
    }//GEN-LAST:event_saveAllButtonActionPerformed

